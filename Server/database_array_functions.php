//Extract ID from main
function GET_INFO(&$DATABASE,$ID)
{
	for($i = 0; $i < sizeof($DATABASE); $i++)
	{
		if($DATABASE[$i]['ID'] == $ID)
			return array($i, $DATABASE[$i]);
	}
	return false;
}
//=============================================================================================================================================
//=============================================================================================================================================
//Check to see if an array is in the DATABASE
function IN_DATABASE(&$DATABASE,$DATA)
{
	for($i = 0; $i < sizeof($DATABASE); $i++)
	{
		if(($DATABASE[$i]['link'] == $DATA['link']) && ($DATABASE[$i]['source'] == $DATA['source']))
		{
			array_push($DATABASE[$i]['hlink'],$DATA['ID']);
			return true;
		}
	}
	return false;
}
//=============================================================================================================================================
//=============================================================================================================================================
//Get all with a particular type
function GET_FROM_DATABASE_TYPE($DATABASE, $type)
{
	$array = array();
	
	foreach($DATABASE as $i)
	{
		if($i['type']==$type)
			array_push($array,$i);
	}
	
	return $array;
}
//=============================================================================================================================================
//=============================================================================================================================================
//Grab any elements that have the ID in there hlink
function GET_FROM_DATABASE_ID_IN_hlink($DATABASE, $ID)
{
	$array = array();
	
	foreach($DATABASE as $i)
	{
		foreach($i['hlink'] as $j)
		{
			if($j == $ID)
			{
				array_push($array,$i);
				break;
			}
		}
	}
	
	return $array;
}
